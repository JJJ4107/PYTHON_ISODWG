    for i, row in enumerate(rows):
        if not row:
            continue
        a1_raw = row[0].strip()
        a1_int = to_int(a1_raw)
        if a1_int == node_no_int:
            matched = True
            i += 1
            while i < len(rows):
                row = rows[i]
                if not row or len(row) < 12:
                    i += 1
                    continue
                a2 = row[1].strip().upper()
